package practice.exercise.chapter3;

public class En312 {
    public static void main(String[] args) {
        System.out.println(1 + 2);

        System.out.println(7 + 7 / 7 + 7);

        System.out.println(7 + 7 * 7 / 7 + 7);

        System.out.println(7 + (7 + 7 * 7 / 7 + 7));

        System.out.println("コメント化されるためエラー");

        System.out.println("データ型が違うためエラー");
    }
    
}
